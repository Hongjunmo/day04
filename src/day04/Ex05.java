package day04;

import java.util.Scanner;

public class Ex05 {
	public static void main(String[] args) {
		
		Scanner scan= new Scanner(System.in);
		
		String str;
		System.out.println("문자열 입력 :");
		str = scan.next();
		switch (str) {
		case "안녕":System.out.println("안녕을 입력");
		break;
		case "그래":System.out.println("그래 를 입력");
		break;
		}
		
	
	}
}
